def get_text(language_code: str, key: str, **kwargs) -> str:
    """
    –ü–æ–ª—É—á–∞–µ—Ç –ø–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –ø–æ –∫–ª—é—á—É –¥–ª—è —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ —è–∑—ã–∫–∞.
    
    Args:
        language_code: –ö–æ–¥ —è–∑—ã–∫–∞ ('ru', 'en', 'pl')
        key: –ö–ª—é—á –ø–µ—Ä–µ–≤–æ–¥–∞
        **kwargs: –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Å—Ç—Ä–æ–∫–∏
    
    Returns:
        –ü–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
    """
    # –ï—Å–ª–∏ —è–∑—ã–∫ –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è, –∏—Å–ø–æ–ª—å–∑—É–µ–º –∞–Ω–≥–ª–∏–π—Å–∫–∏–π
    if language_code not in TRANSLATIONS:
        language_code = 'en'
    
    # –ï—Å–ª–∏ –∫–ª—é—á –Ω–µ –Ω–∞–π–¥–µ–Ω, –∏—Å–ø–æ–ª—å–∑—É–µ–º –∞–Ω–≥–ª–∏–π—Å–∫–∏–π
    if key not in TRANSLATIONS[language_code]:
        if key in TRANSLATIONS['en']:
            language_code = 'en'
        else:
            return f"[MISSING: {key}]"
    
    text = TRANSLATIONS[language_code][key]
    
    # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
    try:
        return text.format(**kwargs)
    except (KeyError, ValueError):
        return text

